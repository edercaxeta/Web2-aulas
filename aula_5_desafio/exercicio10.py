""" 10. Faça um Programa que peça a temperatura em graus Celsius, transforme e mostre em graus
Farenheit. """

grausFare = 0
grausCelsios = float(input("Digite o grau em Celsios: "))

grausFare = (9/5) * grausCelsios + 32

print(f"{grausCelsios}° Celsios é o que Corresponde a {grausFare:.2f}° Farenheit.") 